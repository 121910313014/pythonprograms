# Taking the numbers as input
num1 = int(input("Enter the first number : "))
num2 = int(input("Enter the second number : "))

# Arithmetic operations
add = num1+num2    # addition
sub = num1-num2    # subtraction
prod = num1*num2   # multiplication
div = num1/num2     # divison
mod = num1%num2     # modulus

# Printing the answers
print('The sum of the two number is', add)
print('The difference of the two number is', sub)
print('The product of the two number is', prod)
print('The quotient of the two number is', div)
print('The remainder of the two number is', mod)